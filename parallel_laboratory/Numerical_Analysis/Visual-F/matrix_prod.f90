	PROGRAM matrix
	USE DFPORT
	REAL(4) VAL(2)
	DOUBLE PRECISION MATA(1000,1000),MATB(1000,1000),MATC(1000,1000)
	REAL(4) TARRAY(2)
	INTEGER (4) RA
	DO I=1,1000
	DO J=1,1000
		RA=J
		MATA(I,J)=DRAND(RA)
		RA=I+J+1
		MATB(I,J)=DRAND(RA)
	END DO
	END DO
	DO L=1,10
	VAL(1)=DTIME(TARRAY)
	DO I=1,1000
	DO J=1,1000
		MATC(I,J)=MATA(I,J)*MATB(I,J)
	END DO
	END DO
	VAL(2)=DTIME(TARRAY)
	PRINT *,TARRAY(1),'AND',TARRAY(2),'OR',VAL(2)
	END DO
	END
	